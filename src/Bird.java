public class Bird {
    private float speedY;
    private float y;

    public Bird(float speedY, float y) {
        this.speedY = speedY;
        this.y = y;
    }

    public float getSpeedY() {
        return speedY;
    }

    public void setSpeedY(float speedY) {
        this.speedY = speedY;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}
